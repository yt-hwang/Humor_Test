// Helper function to get coaching data by humor code and MBTI type
export function getHumorMBTICoaching(humorCode: string, mbtiType: string): HumorMBTICoaching | undefined {
  return humorMBTICoachingData.find(
    data => data.humorCode === humorCode.toUpperCase() && data.mbtiType === mbtiType.toUpperCase()
  );
}

// Get all coaching data for a specific humor type
export function getCoachingByHumorType(humorCode: string): HumorMBTICoaching[] {
  return humorMBTICoachingData.filter(data => data.humorCode === humorCode.toUpperCase());
}

// Get all coaching data for a specific MBTI type
export function getCoachingByMBTI(mbtiType: string): HumorMBTICoaching[] {
  return humorMBTICoachingData.filter(data => data.mbtiType === mbtiType.toUpperCase());
}

// Get all available humor codes
export function getAvailableHumorCodes(): string[] {
  return Array.from(new Set(humorMBTICoachingData.map(data => data.humorCode)));
}

// Get coaching data count
export function getCoachingDataCount(): number {
  return humorMBTICoachingData.length;
}
