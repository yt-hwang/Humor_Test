// Helper function to get coaching data by MBTI type
export function getMBTICoaching(mbtiType: string): MBTICoachingData | undefined {
  return mbtiCoachingData.find(data => data.mbtiType === mbtiType.toUpperCase());
}

// Get all coaching data
export function getAllMBTICoaching(): MBTICoachingData[] {
  return mbtiCoachingData;
}

// Get coaching data by MBTI group
export function getMBTICoachingByGroup(group: 'NT' | 'NF' | 'SJ' | 'SP'): MBTICoachingData[] {
  const groupMap: Record<string, string[]> = {
    NT: ['INTJ', 'INTP', 'ENTJ', 'ENTP'],
    NF: ['INFJ', 'INFP', 'ENFJ', 'ENFP'],
    SJ: ['ISTJ', 'ISFJ', 'ESTJ', 'ESFJ'],
    SP: ['ISTP', 'ISFP', 'ESTP', 'ESFP']
  };

  return mbtiCoachingData.filter(data => groupMap[group].includes(data.mbtiType));
}
